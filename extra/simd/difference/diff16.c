// from http://www.alfredklomp.com/programming/sse-intrinsics/
static inline __m128i _mm_cmple_epu16(__m128i x, __m128i y) {
  // Returns 0xFFFF where x <= y:
  return _mm_cmpeq_epi16(_mm_subs_epu16(x, y), _mm_setzero_si128());
}

int32_t difference_vector16(const uint16_t *A, size_t s_a, const uint16_t *B,
                            size_t s_b, uint16_t *C) {

  // we handle the degenerate case
  if (s_a == 0)
    return 0;
  if (s_b == 0) {
    if (A != C)
      memcpy(C, A, sizeof(uint16_t) * s_a);
    return s_a;
  }
  // handle the leading zeroes, it is messy but it allows us to use the fast
  // _mm_cmpistrm instrinsic safely
  int32_t count = 0;
  if ((A[0] == 0) || (B[0] == 0)) {
    if ((A[0] == 0) && (B[0] == 0)) {
      A++;
      s_a--;
      B++;
      s_b--;
    } else if (A[0] == 0) {
      C[count++] = 0;
      A++;
      s_a--;
    } else {
      B++;
      s_b--;
    }
  }

  int32_t i_a = 0, i_b = 0;
  const int vectorlength = sizeof(__m128i) / sizeof(uint16_t);
  const int32_t st_a = (s_a / vectorlength) * vectorlength;
  const int32_t st_b = (s_b / vectorlength) * vectorlength;
  __m128i v_a, v_b, v_bmax;
  if ((i_a < st_a) && (i_b < st_b)) {
    v_a = _mm_lddqu_si128((__m128i *)&A[i_a]);
    v_b = _mm_lddqu_si128((__m128i *)&B[i_b]);
    __m128i runningmask = _mm_setzero_si128();
    const __m128i replicatelast = _mm_set_epi8(15, 14, 15, 14, 15, 14, 15, 14,
                                               15, 14, 15, 14, 15, 14, 15, 14);
    v_bmax = _mm_shuffle_epi8(v_b, replicatelast);
    while (true) {

      const __m128i afoundinb = _mm_cmpistrm(
          v_b, v_a,
          _SIDD_UWORD_OPS | _SIDD_CMP_EQUAL_ANY | _SIDD_MOST_SIGNIFICANT);
      runningmask = _mm_or_si128(runningmask, afoundinb);
      const uint16_t a_max = A[i_a + vectorlength - 1];
      const uint16_t b_max = B[i_b + vectorlength - 1];
      if (a_max <= b_max) {
        const __m128i altb = _mm_cmple_epu16(v_a, v_bmax);
        __m128i finalmask = _mm_andnot_si128(runningmask, altb);
        finalmask = _mm_packs_epi16(finalmask,
                                    _mm_setzero_si128());
        const int r =
            _mm_movemask_epi8(finalmask);
        __m128i sm16 = _mm_load_si128((const __m128i *)shuffle_mask16 + r);
        __m128i p = _mm_shuffle_epi8(v_a, sm16);
        _mm_storeu_si128((__m128i *)&C[count], p); // can overflow
        count += _mm_popcnt_u32(r);
        i_a += vectorlength;
        if (i_a == st_a)
          break;
        runningmask = _mm_setzero_si128();
        v_a = _mm_lddqu_si128((__m128i *)&A[i_a]);
      }
      if (b_max <= a_max) {
        i_b += vectorlength;
        if (i_b == st_b)
          break;
        v_b = _mm_lddqu_si128((__m128i *)&B[i_b]);
        v_bmax = _mm_shuffle_epi8(v_b, replicatelast);
      }
    }
    // at this point, either we have i_a == st_a, which is the end,
    // or we have i_b == st_b
    if (i_a < st_a) { // we have unfinished business...
      uint16_t buffer[8]; // buffer to do a masked load
      memset(buffer, 0, 8 * sizeof(uint16_t));
      memcpy(buffer, B + i_b, (s_b - i_b) * sizeof(uint16_t));
      v_b = _mm_lddqu_si128((__m128i *)buffer);
      while (true) {
        const __m128i afoundinb =
            _mm_cmpistrm(v_b, v_a, _SIDD_UWORD_OPS | _SIDD_CMP_EQUAL_ANY |
                                       _SIDD_MOST_SIGNIFICANT);
        runningmask = _mm_or_si128(runningmask, afoundinb);
        __m128i finalmask = _mm_packs_epi16(
            runningmask, _mm_setzero_si128()); //_mm_packus_epi16
        const int r = _mm_movemask_epi8(finalmask) ^
                      0xFF;
        __m128i sm16 = _mm_load_si128((const __m128i *)shuffle_mask16 + r);
        __m128i p = _mm_shuffle_epi8(v_a, sm16);
        _mm_storeu_si128((__m128i *)&C[count], p); // can overflow
        count += _mm_popcnt_u32(r);
        i_a += vectorlength;
        if (i_a == st_a)
          break;
        runningmask = _mm_setzero_si128();
        v_a = _mm_lddqu_si128((__m128i *)&A[i_a]);
      }
    }
    // at this point we should have i_a == st_a;
  }
  // do the tail using scalar code
  while (i_a < s_a && i_b < s_b) {
    uint16_t a = A[i_a];
    uint16_t b = B[i_b];
    if (b < a) {
      i_b++;
    } else if (a < b) {
      C[count] = a;
      count++;
      i_a++;
    } else { //==
      i_a++;
      i_b++;
    }
  }
  if (i_a < s_a) {
    memmove(C + count, A + i_a, sizeof(uint16_t) * (s_a - i_a));
    count += s_a - i_a;
  }
  return count;
}

