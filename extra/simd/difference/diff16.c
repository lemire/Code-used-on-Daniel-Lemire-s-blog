// assumes a1 and a2 are strictly sorted, writes a1 to a_out with all values in
// a2 removed
// returns the size of the set difference
static int difference(const uint16_t *a1, int length1, const uint16_t *a2,
                      int length2, uint16_t *a_out) {
  int out_card = 0;
  int k1 = 0, k2 = 0;

  if (length1 == 0)
    return 0;

  if (length2 == 0) {
    if (a1 != a_out)
      memcpy(a_out, a1, sizeof(uint16_t) * length1);
    return length1;
  }

  uint16_t s1 = a1[k1];
  uint16_t s2 = a2[k2];

  while (true) {
    if (s1 < s2) {
      a_out[out_card++] = s1;
      ++k1;
      if (k1 >= length1) {
        break;
      }
      s1 = a1[k1];
    } else if (s1 == s2) {
      ++k1;
      ++k2;
      if (k1 >= length1) {
        break;
      }
      if (k2 >= length2) {
        memmove(a_out + out_card, a1 + k1, sizeof(uint16_t) * (length1 - k1));
        return out_card + length1 - k1;
      }
      s1 = a1[k1];
      s2 = a2[k2];
    } else { // if (val1>val2)
      ++k2;
      if (k2 >= length2) {
        memmove(a_out + out_card, a1 + k1, sizeof(uint16_t) * (length1 - k1));
        return out_card + length1 - k1;
      }
      s2 = a2[k2];
    }
  }
  return out_card;
}

