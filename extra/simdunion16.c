// write vector new, while omitting repeated values assuming that previously written vector was "old"
static inline int store_unique(__m128i old,__m128i new, uint16_t *  output) {
    __m128i vecTmp = _mm_alignr_epi8(new, old, 16 - 2);
    int M = _mm_movemask_epi8(_mm_cmpeq_epi16(vecTmp,new));
    M = _pext_u32(M,0x5555);
    int numberofnewvalues = 8 - _mm_popcnt_u32(M);
    __m128i key =  _mm_lddqu_si128((const __m128i* )uniqshuf + M);
    __m128i val =  _mm_shuffle_epi8(new,key);
    _mm_storeu_si128((__m128i* )output,val);
    return numberofnewvalues;
}


// working in-place, this function overwrites the repeated values
static uint32_t unique(uint16_t *  out, uint32_t len) {
    uint32_t pos = 1;
    for(uint32_t i = 1; i < len; ++i) {
        if(out[i] != out[i-1]) {
            out[pos++] = out[i];
        }
    }
    return pos;
}


// the simple union algo. first compute a union with repeated values, then uses the "unique" function (as a second pass) to prune them out.
static uint32_t sse_unite_simple(uint16_t * __restrict__ array1, uint32_t length1 ,
                                 uint16_t *  __restrict__ array2, uint32_t length2, uint16_t *  __restrict__  output) {
    __m128i vA, vB, V, vecMin,vecMax;
    uint16_t * initoutput = output;
    assert(length1 % 8 == 0); // because I am lazy
    assert(length2 % 8 == 0); // because I am lazy
    assert(length1 != 0); // because I am lazy
    assert(length2 != 0); // because I am lazy
    uint32_t len1 = length1 / 8;
    uint32_t len2 = length2 / 8;
    uint32_t pos1 = 0;
    uint32_t pos2 = 0;
    // we start the machine
    vA = _mm_lddqu_si128((const __m128i* )array1 + pos1);
    pos1++;
    vB = _mm_lddqu_si128((const __m128i* )array2 + pos2);
    pos2++;
    uint16_t curA, curB;
    curA = (pos1 < len1) ? array1[8*pos1] : 0;
    curB = (pos2 < len2) ? array2[8*pos2] : 0;
    sse_merge(&vA,&vB,&vecMin,&vecMax);
    _mm_storeu_si128((__m128i* )output,vecMin);
    output+= 8;
    while((pos1 < len1) && (pos2<len2)) {
        // code is not optimal, too much branching?
        if(curA <= curB) {
            V = _mm_lddqu_si128((const __m128i* )array1 + pos1);
            pos1++;
            curA = (pos1 < len1) ? array1[8*pos1] : 0;
        } else {
            V = _mm_lddqu_si128((const __m128i* )array2 + pos2);
            pos2++;
            curB = (pos2 < len2) ? array2[8*pos2] : 0;
        }
        sse_merge(&V,&vecMax,&vecMin,&vecMax);
        _mm_storeu_si128((__m128i* )output,vecMin);
        output+= 8;
    }
    //
    while(pos1<len1) {
        V = _mm_lddqu_si128((const __m128i* )array1 + pos1);
        pos1++;
        sse_merge(&V,&vecMax,&vecMin,&vecMax);
        _mm_storeu_si128((__m128i* )output,vecMin);
        output+= 8;
    }
    while(pos2<len2) {
        V = _mm_lddqu_si128((const __m128i* )array2 + pos2);
        pos2++;
        sse_merge(&V,&vecMax,&vecMin,&vecMax);
        _mm_storeu_si128((__m128i* )output,vecMin);
        output+=8;
    }
    _mm_storeu_si128((__m128i* )output,vecMax);
    output+= 8;
    uint32_t len = output-initoutput;
    return unique(initoutput, len);
}

// a one-pass SSE union algorithm
static uint32_t sse_unite(uint16_t * __restrict__ array1, uint32_t length1 ,
                          uint16_t *  __restrict__ array2, uint32_t length2, uint16_t *  __restrict__  output) {
    __m128i vA, vB, V, vecMin,vecMax;
    __m128i laststore;
    uint16_t * initoutput = output;
    assert(length1 % 8 == 0); // because I am lazy
    assert(length2 % 8 == 0); // because I am lazy
    assert(length1 != 0); // because I am lazy
    assert(length2 != 0); // because I am lazy
    uint32_t len1 = length1 / 8;
    uint32_t len2 = length2 / 8;
    uint32_t pos1 = 0;
    uint32_t pos2 = 0;
    // we start the machine
    vA = _mm_lddqu_si128((const __m128i* )array1 + pos1);
    pos1++;
    vB = _mm_lddqu_si128((const __m128i* )array2 + pos2);
    pos2++;
    uint16_t curA, curB;
    curA = (pos1 < len1) ? array1[8*pos1] : 0;
    curB = (pos2 < len2) ? array2[8*pos2] : 0;
    sse_merge(&vA,&vB,&vecMin,&vecMax);
    laststore = _mm_set1_epi32(array1[0] < array2[0] ? array1[0] -1 : array2[0] -1);
    output+= store_unique(laststore,vecMin, output);
    laststore=vecMin;
    while((pos1 < len1) && (pos2<len2)) {
        // code is not optimal, too much branching?
        if(curA <= curB) {
            V = _mm_lddqu_si128((const __m128i* )array1 + pos1);
            pos1++;
            curA = (pos1 < len1) ? array1[8*pos1] : 0;
        } else {
            V = _mm_lddqu_si128((const __m128i* )array2 + pos2);
            pos2++;
            curB = (pos2 < len2) ? array2[8*pos2] : 0;
        }
        sse_merge(&V,&vecMax,&vecMin,&vecMax);
        output+= store_unique(laststore,vecMin, output);
        laststore = vecMin;
    }
    while(pos1<len1) {
        V = _mm_lddqu_si128((const __m128i* )array1 + pos1);
        pos1++;
        sse_merge(&V,&vecMax,&vecMin,&vecMax);
        output+= store_unique(laststore,vecMin, output);
        laststore = vecMin;
    }
    while(pos2<len2) {
        V = _mm_lddqu_si128((const __m128i* )array2 + pos2);
        pos2++;
        sse_merge(&V,&vecMax,&vecMin,&vecMax);
        output+= store_unique(laststore,vecMin, output);
        laststore = vecMin;
    }
    output+= store_unique(laststore,vecMax,output);

    uint32_t len = output-initoutput;

    return len;
}


// standard scalar algorithm
uint32_t union_uint32(const uint16_t *set_1, uint32_t size_1, const uint16_t *set_2,
                      uint32_t size_2, uint16_t *buffer) {
    uint32_t pos = 0, idx_1 = 0, idx_2 = 0;

    if (0 == size_2) {
        memcpy(buffer, set_1, size_1 * sizeof(uint16_t));
        return size_1;
    }
    if (0 == size_1) {
        memcpy(buffer, set_2, size_2 * sizeof(uint16_t));
        return size_2;
    }

    uint16_t val_1 = set_1[idx_1], val_2 = set_2[idx_2];

    while (true) {
        if (val_1 < val_2) {
            buffer[pos++] = val_1;
            ++idx_1;
            if (idx_1 >= size_1) break;
            val_1 = set_1[idx_1];
        } else if (val_2 < val_1) {
            buffer[pos++] = val_2;
            ++idx_2;
            if (idx_2 >= size_2) break;
            val_2 = set_2[idx_2];
        } else {
            buffer[pos++] = val_1;
            ++idx_1;
            ++idx_2;
            if (idx_1 >= size_1 || idx_2 >= size_2) break;
            val_1 = set_1[idx_1];
            val_2 = set_2[idx_2];
        }
    }
    if (idx_1 < size_1) {
        const uint32_t n_elems = size_1 - idx_1;
        memcpy(buffer + pos, set_1 + idx_1, n_elems * sizeof(uint16_t));
        pos += n_elems;
    } else if (idx_2 < size_2) {
        const uint32_t n_elems = size_2 - idx_2;
        memcpy(buffer + pos, set_2 + idx_2, n_elems * sizeof(uint16_t));
        pos += n_elems;
    }
    return pos;
}

static int uint16_compare (const void * a, const void * b) {
    return ( *(uint16_t*)a - *(uint16_t*)b );
}

// a one-pass SSE union algorithm
static uint32_t sse_unite_opti(uint16_t * __restrict__ array1, uint32_t length1 ,
                               uint16_t *  __restrict__ array2, uint32_t length2, uint16_t *  __restrict__  output) {
    __m128i vA, vB, V, vecMin,vecMax;
    __m128i laststore;
    uint16_t * initoutput = output;
    if (0 == length1) {
        memcpy(output, array1, length1 * sizeof(uint16_t));
        return length1;
    }
    if (0 == length2) {
        memcpy(output, array2, length2 * sizeof(uint16_t));
        return length2;
    }
    uint32_t len1 = length1 / 8;
    uint32_t len2 = length2 / 8;
    uint32_t pos1 = 0;
    uint32_t pos2 = 0;
    // we start the machine
    vA = _mm_lddqu_si128((const __m128i* )array1 + pos1);
    pos1++;
    vB = _mm_lddqu_si128((const __m128i* )array2 + pos2);
    pos2++;
    sse_merge(&vA,&vB,&vecMin,&vecMax);
    laststore = _mm_set1_epi32(array1[0] < array2[0] ? array1[0] -1 : array2[0] -1);
    output+= store_unique(laststore,vecMin, output);
    laststore=vecMin;
    if((pos1 < len1) && (pos2<len2)) {
        uint16_t curA, curB;
        curA = array1[8*pos1];
        curB = array2[8*pos2];
        while(true) {
            if(curA <= curB) {
                V = _mm_lddqu_si128((const __m128i* )array1 + pos1);
                pos1++;
                if(pos1 < len1 ) {
                    curA =  array1[8*pos1];
                } else {
                    break;
                }
            } else {
                V = _mm_lddqu_si128((const __m128i* )array2 + pos2);
                pos2++;
                if(pos2 < len2 ) {
                    curB =  array2[8*pos2];
                } else {
                    break;
                }
            }
            sse_merge(&V,&vecMax,&vecMin,&vecMax);
            output+= store_unique(laststore,vecMin, output);
            laststore = vecMin;
        }
        sse_merge(&V,&vecMax,&vecMin,&vecMax);
        output+= store_unique(laststore,vecMin, output);
        laststore = vecMin;
    }
    // we finish the rest off using a scalar algorithm
    // copy the small end on a tmp buffer
    uint32_t len = (uint32_t) (output-initoutput);
    uint16_t buffer[16];
    uint32_t leftoversize = store_unique(laststore,vecMax, buffer);
    if(pos1 == len1) {
        memcpy(buffer + leftoversize, array1 + 8 * pos1, (length1 - 8 * len1) * sizeof(uint16_t));
        leftoversize += length1 - 8 * len1;
        qsort (buffer, leftoversize, sizeof(uint16_t), uint16_compare);


        leftoversize= unique(buffer, leftoversize);
        len += union_uint32(buffer,leftoversize, array2 + 8 * pos2,
                            length2 - 8 * pos2, output);
    } else {
        memcpy(buffer + leftoversize, array2 + 8 * pos2, (length2 - 8 * len2) * sizeof(uint16_t));
        leftoversize += length2 - 8 * len2;
        qsort (buffer, leftoversize, sizeof(uint16_t), uint16_compare);
        leftoversize= unique(buffer, leftoversize);
        len += union_uint32(buffer,leftoversize, array1 + 8 * pos1,
                            length1 - 8 * pos1, output);
    }
    return len;

}






