// print array (duh)
static void array_print(const uint16_t * x, uint32_t len) {
    for(uint32_t i = 0; i < len; i++) {
        printf("%u,",x[i]);
    }
    printf("\n");
}

// check that array is sorted (duh)
static bool array_is_sorted(const uint16_t * x, uint32_t len) {
    for(uint32_t i = 1; i < len; i++) {
        if(x[i] < x[i-1]) return false;
    }
    return true;
}


// check that arrays are equal
static bool arrays_are_equal(const uint16_t * x,const uint16_t * y, uint32_t len) {
    for(uint32_t i = 0; i < len; i++) {
        if(x[i] != y[i]) return false;
    }
    return true;
}

// check that array contains strictly sorted 16-bit values
static bool array_is_strictly_sorted(const uint16_t * x, uint32_t len) {
    for(uint32_t i = 1; i < len; i++) {
        if(x[i] <= x[i-1]) return false;
    }
    return true;
}


// check that vector contains sorted 16-bit values
static bool is_sorted(__m128i x) {
    return ( (uint16_t)_mm_extract_epi16(x,0)<=(uint16_t)_mm_extract_epi16(x,1))
           && ( (uint16_t)_mm_extract_epi16(x,1)<=(uint16_t)_mm_extract_epi16(x,2))
           && ( (uint16_t)_mm_extract_epi16(x,2)<=(uint16_t)_mm_extract_epi16(x,3))
           && ( (uint16_t)_mm_extract_epi16(x,3)<=(uint16_t)_mm_extract_epi16(x,4))
           && ( (uint16_t)_mm_extract_epi16(x,4)<=(uint16_t)_mm_extract_epi16(x,5))
           && ( (uint16_t)_mm_extract_epi16(x,5)<=(uint16_t)_mm_extract_epi16(x,6))
           && ( (uint16_t)_mm_extract_epi16(x,6)<=(uint16_t)_mm_extract_epi16(x,7));
}

// print vector as if it were an array of 16-bit unsigned integers
static void print16(__m128i x) {
    printf("%u %u %u %u %u %u %u %u \n", _mm_extract_epi16(x,0),_mm_extract_epi16(x,1),_mm_extract_epi16(x,2),_mm_extract_epi16(x,3)
           , _mm_extract_epi16(x,4),_mm_extract_epi16(x,5),_mm_extract_epi16(x,6),_mm_extract_epi16(x,7));
}

// Assuming that vInput1 and vInput2 are sorted, produces a sorted output going from vecMin all the way to vecMax
// developed originally for merge sort using SIMD instructions.
// Standard merge. See, e.g., Inoue and Taura, SIMD- and Cache-Friendly Algorithm for Sorting an Array of Structures
static void sse_merge(__m128i *vInput1, __m128i *vInput2, // input 1 & 2
                      __m128i *vecMin, __m128i *vecMax) { // output
    __m128i vecTmp;
    vecTmp = _mm_min_epu16(*vInput1, *vInput2);
    *vecMax = _mm_max_epu16(*vInput1, *vInput2);
    vecTmp = _mm_alignr_epi8(vecTmp, vecTmp, 2);
    *vecMin = _mm_min_epu16(vecTmp, *vecMax);
    *vecMax = _mm_max_epu16(vecTmp, *vecMax);
    vecTmp = _mm_alignr_epi8(*vecMin, *vecMin, 2);
    *vecMin = _mm_min_epu16(vecTmp, *vecMax);
    *vecMax = _mm_max_epu16(vecTmp, *vecMax);
    vecTmp = _mm_alignr_epi8(*vecMin, *vecMin, 2);
    *vecMin = _mm_min_epu16(vecTmp, *vecMax);
    *vecMax = _mm_max_epu16(vecTmp, *vecMax);
    vecTmp = _mm_alignr_epi8(*vecMin, *vecMin, 2);
    *vecMin = _mm_min_epu16(vecTmp, *vecMax);
    *vecMax = _mm_max_epu16(vecTmp, *vecMax);
    vecTmp = _mm_alignr_epi8(*vecMin, *vecMin, 2);
    *vecMin = _mm_min_epu16(vecTmp, *vecMax);
    *vecMax = _mm_max_epu16(vecTmp, *vecMax);
    vecTmp = _mm_alignr_epi8(*vecMin, *vecMin, 2);
    *vecMin = _mm_min_epu16(vecTmp, *vecMax);
    *vecMax = _mm_max_epu16(vecTmp, *vecMax);
    vecTmp = _mm_alignr_epi8(*vecMin, *vecMin, 2);
    *vecMin = _mm_min_epu16(vecTmp, *vecMax);
    *vecMax = _mm_max_epu16(vecTmp, *vecMax);
    *vecMin = _mm_alignr_epi8(*vecMin, *vecMin, 2);
}



