void unittesting(uint32_t len1, uint32_t len2) {
    assert(len1 % 8 == 0);
    uint16_t * array1= malloc(len1 * sizeof(uint16_t));
    assert(len2 % 8 == 0);
    uint16_t * array2= malloc(len2 * sizeof(uint16_t));
    uint16_t last;
    last = 0;
    for(int k = 0; k < len1; ++k) {
        last = last + (rand()&0x0F) + 1;
        array1[k] =last;
    }
    assert(array_is_strictly_sorted(array1,len1));

    last = 0;
    for(int k = 0; k < len2; ++k) {
        last = last + (rand()&0x0F) + 1;
        array2[k] = last;
    }
    assert(array_is_strictly_sorted(array2,len2));

    uint16_t * output = malloc((len1 + len2) * sizeof(uint16_t));
    uint32_t len = sse_unite(array1, len1,array2, len2,output);
    for(int k = 0 ; k < len; ++k) {
        if(k>0) {
            if(output[k]<=output[k-1]) {
                printf("bug SSE at k =%d out of %d:  %u %u",k,len,output[k-1],output[k]);
                abort();
            }
        }
    }
    uint16_t * outputs = malloc((len1 + len2) * sizeof(uint16_t));
    uint32_t lens = sse_unite_simple(array1, len1,array2, len2,outputs);
    for(int k = 0 ; k < lens; ++k) {
        if(k>0) {
            if(outputs[k]<=outputs[k-1]) {
                printf("bug SSE simple at k =%d out of %d:  %u %u",k,lens,outputs[k-1],outputs[k]);
                abort();
            }
        }
    }
    uint16_t * outputopti = malloc((len1 + len2) * sizeof(uint16_t));
    uint32_t lenopti = sse_unite_opti(array1, len1,array2, len2,outputopti);
    for(int k = 0 ; k < lenopti; ++k) {
        if(k>0) {
            if(outputopti[k]<=outputopti[k-1]) {
                printf("bug opti SSE at k =%d out of %d:  %u %u",k,lenopti,outputopti[k-1],outputopti[k]);
                abort();
            }
        }
    }


    uint16_t * output2 = malloc((len1 + len2) * sizeof(uint16_t));

    uint32_t lenslow =  union_uint32(array1, len1,array2, len2,output2);
    if(len != lenslow) {
        printf("bad length = %d %d \n", len,lenslow);
        array_print(output,len);
        array_print(output2,lenslow);
        abort();
    }
    if(lens != lenslow) {
        printf("bad s length = %d %d \n", lens,lenslow);
        array_print(outputs,lens);
        array_print(output2,lenslow);
        abort();
    }
    if(lenopti != lenslow) {
        printf("bad opti length = %d %d \n", lenopti,lenslow);
        array_print(outputopti,lenopti);
        array_print(output2,lenslow);
        abort();
    }
    for(int k = 0 ; k < len; ++k) {
        if(output[k]!=output2[k]) {
            printf("bug SSE at k =%d out of %d:  %u %u",k,len,output[k],output2[k]);
            abort();
        }
        if(outputs[k]!=output2[k]) {
            printf("bug simple SSE at k =%d out of %d:  %u %u",k,len,outputs[k],output2[k]);
            abort();
        }
        if(outputopti[k]!=output2[k]) {
            printf("bug opti SSE at k =%d out of %d:  %u %u",k,len,outputopti[k],output2[k]);
            abort();
        }
    }
    free(output2);
    free(outputs);
    free(outputopti);
    free(output);
    free(array1);
    free(array2);
}


void benchmark(uint32_t len1, uint32_t len2) {
    printf("== benchmark sizes = %d %d \n", len1, len2);
    assert(len1 % 8 == 0);
    assert(len2 % 8 == 0);
    uint16_t * array1= malloc(len1 * sizeof(uint16_t));
    uint16_t * array2= malloc(len2 * sizeof(uint16_t));
    uint16_t last;
    last = 0;
    for(int k = 0; k < len1; ++k) {
        last = last + (rand()&0x0F) + 1;
        array1[k] =last;
    }
    assert(array_is_strictly_sorted(array1,len1));

    last = 0;
    for(int k = 0; k < len2; ++k) {
        last = last + (rand()&0x0F) + 1;
        array2[k] = last;
    }
    assert(array_is_strictly_sorted(array2,len2));

    uint16_t * output1 = malloc((len1+len2) * sizeof(uint16_t));
    uint16_t * output2 = malloc((len1+len2) * sizeof(uint16_t));
    uint16_t * output3 = malloc((len1+len2) * sizeof(uint16_t));
    uint16_t * output4 = malloc((len1+len2) * sizeof(uint16_t));

    uint64_t cycles_start = 0, cycles_final = 0;
    RDTSC_START(cycles_start);
    uint32_t lensse = sse_unite(array1, len1,array2, len2,output1);
    RDTSC_FINAL(cycles_final);
    printf("SSE took %" PRIu64 " cycles\n",
           cycles_final - cycles_start);
    RDTSC_START(cycles_start);
    uint32_t optilensse = sse_unite_opti(array1, len1,array2, len2,output2);
    RDTSC_FINAL(cycles_final);
    printf("opti SSE took %" PRIu64 " cycles\n",
           cycles_final - cycles_start);

    RDTSC_START(cycles_start);
    uint32_t slensse = sse_unite_simple(array1, len1,array2, len2,output3);
    RDTSC_FINAL(cycles_final);
    printf("simple SSE took %" PRIu64 " cycles\n",
           cycles_final - cycles_start);

    RDTSC_START(cycles_start);
    uint32_t lenslow =  union_uint32(array1, len1,array2, len2,output4);
    RDTSC_FINAL(cycles_final);
    printf("scalar took %" PRIu64 " cycles\n",
           cycles_final - cycles_start);
    printf("lensse = %d slensse = %d lenslow = %d \n", lensse, slensse,lenslow);
    assert(lensse == lenslow);
    assert(slensse == lenslow);
    assert(optilensse == lenslow);

    assert(arrays_are_equal(output1,output4,lenslow));
    assert(arrays_are_equal(output2,output4,lenslow));
    assert(arrays_are_equal(output3,output4,lenslow));

    free(output1);
    free(output2);
    free(output3);
    free(output4);
    free(array1);
    free(array2);
}

